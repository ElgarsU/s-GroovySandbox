class Math {
    static void main(String[] args) {

        println("5 + 4 = " + (5 + 4))
        println("5 - 4 = " + (5 - 4))
        println("5 * 4 = " + (5 * 4))
        println("5 / 4 = " + (5 / 4))
        println("5 % 4 = " + (5 % 4))

        println("5.2 + 4.4 = " + (5.2 + 4.4))
        println("5.2 - 4.4 = " + (5.2 - 4.4))
        println("5.2 * 4.4 = " + (5.2 * 4.4))
        println("5.2 / 4.4 = " + (5.2 / 4.4))


    }
}
